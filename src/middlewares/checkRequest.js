const checkRequest = async (req, res, next) => {
    const params = req.params;
    const query = req.query;
    const body = req.body;
    const url = req.url;

    console.log(
`---------------------------------------------------------------------------------------------------------------------------------------------
    Today ${new Date()} a query has been received at " ${url} "
---------------------------------------------------------------------------------------------------------------------------------------------`)
    if (Object.values(params) != "" || undefined) {
        console.log(
`---------------------------------------------------------------------------------------------------------------------------------------------
    Trae Data Params : ${JSON.stringify(params.email)}
---------------------------------------------------------------------------------------------------------------------------------------------`)
    };
    if (Object.values(query) != "" || undefined) {
        console.log(
`---------------------------------------------------------------------------------------------------------------------------------------------
    Trae Data Query : ${JSON.stringify(query)}
---------------------------------------------------------------------------------------------------------------------------------------------`)
    };
    if (Object.values(body) != "" || undefined) {
        console.log(
`---------------------------------------------------------------------------------------------------------------------------------------------
    Trae Data Body : ${JSON.stringify(body)}
---------------------------------------------------------------------------------------------------------------------------------------------`)
    };
    next();
};

module.exports = { checkRequest };